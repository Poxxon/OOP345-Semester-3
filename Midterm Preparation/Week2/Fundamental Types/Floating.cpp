#include <iostream>
int main() {
    float f = 3.14f;     // single precision
    double d = 2.71828;  // double precision
    long double ld = 1.234567890123456789L; // long double

    std::cout << "Float: " << f << std::endl;
    std::cout << "Double: " << d << std::endl;
    std::cout << "Long double: " << ld << std::endl;

    return 0;
}
